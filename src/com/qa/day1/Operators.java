package com.qa.day1;

public class Operators {

	// Arithmetic Operators:
	// > +
	// > -
	// > /
	// > *
	// > % - Gives the remainder

	// Unary Operators:
	// > ++ - increments by 1
	// > -- - decrements by 1
	// > + - positive value
	// > - - negative value
	// > ! - not operator (can use with booleans)
	
	public static int power(int a) {
		return a*a;
	}

}
